import pytest
from shapely import wkt
from shapely.errors import GEOSException

from mapchete.bounds import Bounds
from mapchete.geometry.clip import clip_by_bounds


def test_clip_by_bounds():
    geometry = Bounds(0, 0, 2, 2).geometry
    bounds = Bounds(0, 0, 1, 1)

    clipped = clip_by_bounds(geometry=geometry, bounds=bounds)

    assert clipped.bounds == (0, 0, 1, 1)


